const langSelect = document.getElementById('langSelect');

function setLanguage(lang) {

  // console.log("caleed")

  // Translate text content
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = translations[lang][key];
  });

  // Translate placeholders
  const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
  placeholders.forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang] && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });


  const emailInput = document.getElementById('email_input');

  if(emailInput) {
    emailInput.placeholder = translations[lang]['email_input_placeholder'];
  }

  const mobileSelectElement = document.getElementById('mobile_langSelect');
  const pcSelectElement = document.getElementById('langSelect');

  console.log('Mobile select value:', mobileSelectElement.value, lang);
  if(mobileSelectElement.value !== lang) {
    document.getElementById('mobile_langSelect').value = lang; 
    console.log('Mobile select value:', mobileSelectElement.value);
  }
  else if (pcSelectElement.value !== lang) {
    document.getElementById('langSelect').value = lang;
    console.log('PC select value:', pcSelectElement.value);
  }
  
  
  // Save preference
  localStorage.setItem('language', lang);
}

